entry:
		;-------------------------------
		; BPB (BIOS Parameter Block)
		;-------------------------------
		jmp		ipl
		times	90-($-$$) db 0x90

		;-------------------------------
		; IPL (Initial Program Loader)
		;-------------------------------
ipl:
		jmp		$

		;-------------------------------
		; Boot Flag
		;-------------------------------
		times	510-($-$$) db 0x00
		db		0x55,0xaa
